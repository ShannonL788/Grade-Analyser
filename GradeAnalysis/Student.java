public class Student {
    String id;
    double finalScore, labExam, examPercent, labPercent;

    public Student(String id, double finalScore, double labExam, double examPercent, double labPercent) {
        this.id = id;
        this.finalScore = finalScore;
        this.labExam = labExam;
        this.examPercent = examPercent;
        this.labPercent = labPercent;
    }
}


